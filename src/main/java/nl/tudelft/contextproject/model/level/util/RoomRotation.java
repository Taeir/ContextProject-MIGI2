package nl.tudelft.contextproject.model.level.util;

/**
 * Enum for possible Rotations of rooms.
 *
 */
public enum RoomRotation {

	/**
	 * Don't rotate at all.
	 */
	ROTATION_0,
	
	/**
	 * Rotate 90 degrees.
	 */
	ROTATION_90,
	
	/**
	 * Rotate 180 degrees, flip.
	 */
	ROTATION_180,
	
	/**
	 * Rotate 240 degrees.
	 */
	ROTATION_240;
}
